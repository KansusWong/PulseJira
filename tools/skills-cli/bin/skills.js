#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

function printHelp() {
  console.log(`Usage:
  skills add <skill-id> --agent <agent-id> [--description "<text>"] [--force]

Examples:
  npx skills add api-versioning --agent tech-lead --description "Guide API version rollout"
  npx skills add incident-triage --agent developer --force
`);
}

function fail(message) {
  console.error(`[skills] ${message}`);
  process.exit(1);
}

function parseArgs(argv) {
  const positionals = [];
  const flags = {};

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith('--')) {
      positionals.push(token);
      continue;
    }
    const key = token.slice(2);
    if (key === 'force') {
      flags.force = true;
      continue;
    }
    const value = argv[i + 1];
    if (!value || value.startsWith('--')) {
      fail(`Missing value for --${key}`);
    }
    flags[key] = value;
    i += 1;
  }

  return { positionals, flags };
}

function sanitizeId(raw) {
  return String(raw || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function toPascalCase(raw) {
  return raw
    .split('-')
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join('');
}

function readJson(filePath, fallback) {
  try {
    if (fs.existsSync(filePath)) {
      return JSON.parse(fs.readFileSync(filePath, 'utf-8'));
    }
  } catch (e) {
    fail(`Failed to read ${filePath}: ${e.message}`);
  }
  return fallback;
}

function writeJson(filePath, value) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, JSON.stringify(value, null, 2), 'utf-8');
}

function createSkillModule({ filePath, skillId, description, agentId, force }) {
  const exists = fs.existsSync(filePath);
  if (exists && !force) {
    return { created: false, overwritten: false };
  }

  const fnName = `run${toPascalCase(skillId)}Skill`;
  const content = `import type { AgentContext } from '@/lib/core/types';

/**
 * ${description}
 * Auto-generated by \`npx skills add\`.
 */
export async function ${fnName}(input: string, _context: AgentContext = {}) {
  return {
    skill: '${skillId}',
    agent: '${agentId}',
    status: 'not_implemented',
    input,
  };
}
`;
  fs.writeFileSync(filePath, content, 'utf-8');
  return { created: !exists, overwritten: exists };
}

function createSkillMd({ filePath, skillId, description, agentId, force }) {
  const exists = fs.existsSync(filePath);
  if (exists && !force) {
    return { created: false, overwritten: false };
  }

  const content = `---
name: ${skillId}
description: ${description}
version: 1.0.0
requires:
  tools: []
tags: [agent-${agentId}]
---
## Instructions

Implement the \`${skillId}\` workflow for agent \`${agentId}\`.

Required behavior:
- Define trigger conditions.
- Describe step-by-step execution strategy.
- Specify expected output format.
`;
  fs.writeFileSync(filePath, content, 'utf-8');
  return { created: !exists, overwritten: exists };
}

function updateAgentOverrides({ overridesPath, agentId, skillId, description }) {
  const overrides = readJson(overridesPath, {});
  const entry = overrides[agentId] && typeof overrides[agentId] === 'object'
    ? overrides[agentId]
    : { skills: [] };

  const skills = Array.isArray(entry.skills) ? entry.skills.slice() : [];
  const existingIndex = skills.findIndex((s) => s && s.name === skillId);
  if (existingIndex >= 0) {
    skills[existingIndex] = { name: skillId, description };
  } else {
    skills.push({ name: skillId, description });
  }

  overrides[agentId] = { ...entry, skills };
  writeJson(overridesPath, overrides);
}

function runAdd(argv) {
  const { positionals, flags } = parseArgs(argv);
  const rawSkillId = positionals[0];
  const rawAgentId = flags.agent;
  if (!rawSkillId) fail('Missing <skill-id>.');
  if (!rawAgentId) fail('Missing --agent <agent-id>.');

  const skillId = sanitizeId(rawSkillId);
  const agentId = sanitizeId(rawAgentId);
  if (!skillId) fail(`Invalid skill id: "${rawSkillId}"`);
  if (!agentId) fail(`Invalid agent id: "${rawAgentId}"`);

  const description = String(flags.description || `Skill for ${agentId}: ${skillId}`).trim();
  const force = !!flags.force;

  const root = process.cwd();
  const agentsDir = path.join(root, 'agents');
  const agentDir = path.join(agentsDir, agentId);

  if (!fs.existsSync(agentsDir)) {
    fail(`Expected agents directory in current workspace: ${agentsDir}`);
  }
  if (!fs.existsSync(agentDir) || !fs.statSync(agentDir).isDirectory()) {
    fail(`Agent "${agentId}" was not found at ${agentDir}`);
  }

  const agentSkillsDir = path.join(agentDir, 'skills');
  fs.mkdirSync(agentSkillsDir, { recursive: true });

  const skillModulePath = path.join(agentSkillsDir, `${skillId}.ts`);
  const moduleResult = createSkillModule({
    filePath: skillModulePath,
    skillId,
    description,
    agentId,
    force,
  });

  const skillDir = path.join(root, 'skills', skillId);
  fs.mkdirSync(skillDir, { recursive: true });
  const skillMdPath = path.join(skillDir, 'SKILL.md');
  const skillMdResult = createSkillMd({
    filePath: skillMdPath,
    skillId,
    description,
    agentId,
    force,
  });

  const overridesPath = path.join(agentsDir, 'agent-skill-overrides.json');
  updateAgentOverrides({
    overridesPath,
    agentId,
    skillId,
    description,
  });

  console.log(`[skills] Added "${skillId}" to agent "${agentId}".`);
  console.log(`[skills] Module: ${skillModulePath} (${moduleResult.created ? 'created' : moduleResult.overwritten ? 'overwritten' : 'kept'})`);
  console.log(`[skills] SKILL.md: ${skillMdPath} (${skillMdResult.created ? 'created' : skillMdResult.overwritten ? 'overwritten' : 'kept'})`);
  console.log(`[skills] Overrides updated: ${overridesPath}`);
}

function main() {
  const argv = process.argv.slice(2);
  const command = argv[0];

  if (!command || command === '--help' || command === '-h') {
    printHelp();
    return;
  }

  if (command !== 'add') {
    fail(`Unsupported command "${command}". Only "add" is available in V1.`);
  }

  runAdd(argv.slice(1));
}

main();
